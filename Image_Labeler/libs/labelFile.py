class LabelFile(object):
    # It might be changed as window creates. By default, using XML ext
    # suffix = '.lif'
    suffix = XML_EXT

    def __init__(self, filename=None):
        self.shapes = ()
        self.imagePath = None
        self.imageData = None
        self.verified = False

    def savePascalVocFormat(self, filename, shapes, imagePath, imageData,
                            lineColor=None, fillColor=None, databaseSrc=None):

        optParser = OptionParser("usage: %prog [options] images... ")
        optParser.add_option("-p","--prefix",help="New File Prefix Date Format",action="store",type="string",dest="dateFormat", default="%Y-%m-%d %H:%M:%S")
        optParser.add_option("-d","--dry",help="Only show the rename, NO MOVE WILL BE PERFORMED",action="store_true",dest='dry')
        (options,args) = optParser.parse_args(sys.argv)
        dateFormat = options.dateFormat

        imgFolderPath = os.path.dirname(imagePath)
        imgFolderName = os.path.split(imgFolderPath)[-1]
        imgFileName = os.path.basename(imagePath)
        try:
            img = Image.open(imagePath)
            exif_data = img._getexif()
            strImageDate = exif_data[306]
            imgTime = time.strftime(dateFormat,time.strptime(strImageDate,"%Y:%m:%d %H:%M:%S"))
            imgDate = imgTime
            #imgTime = datetime.fromtimestamp(getmtime(imagePath)).strftime('%T')
            #imgDate = datetime.fromtimestamp(getmtime(imagePath)).strftime('%m/%d/%Y')

        except OSError:
            imgTime = '0'
            imgDate = '0'
        #imgFileNameWithoutExt = os.path.splitext(imgFileName)[0]
        # Read from file path because self.imageData might be empty if saving to
        # Pascal format
        image = QImage()
        image.load(imagePath)
        imageShape = [image.height(), image.width(),
                      1 if image.isGrayscale() else 3]
        writer = PascalVocWriter(imgFolderName, imgFileName, imgTime, imgDate,
                                 imageShape, localImgPath=imagePath)
        writer.verified = self.verified

        for shape in shapes:
            points = shape['points']
            label = shape['label']
            # Add Chris
            difficult = int(shape['difficult'])
            bndbox = LabelFile.convertPoints2BndBox(points)
            writer.addBndBox(bndbox[0], bndbox[1], bndbox[2], bndbox[3], label, difficult)

        predictions,nnnames = dm.getPredictions(filename[:filename.rfind('/')+1]+imagePath.split('\\')[-2]+'/'+filename.split('/')[-1])
        for i in range(len(predictions)):
            writer.addPrediction(predictions[i],nnnames[i])

        writer.save(targetFile=filename[:filename.rfind('/')+1]+imagePath.split('\\')[-2]+'/'+filename.split('/')[-1])
        return

    def saveYoloFormat(self, filename, shapes, imagePath, imageData, classList,
                            lineColor=None, fillColor=None, databaseSrc=None):
        imgFolderPath = os.path.dirname(imagePath)
        imgFolderName = os.path.split(imgFolderPath)[-1]
        imgFileName = os.path.basename(imagePath)
        #imgFileNameWithoutExt = os.path.splitext(imgFileName)[0]
        # Read from file path because self.imageData might be empty if saving to
        # Pascal format
        image = QImage()
        image.load(imagePath)
        imageShape = [image.height(), image.width(),
                      1 if image.isGrayscale() else 3]
        writer = YOLOWriter(imgFolderName, imgFileName,
                                 imageShape, localImgPath=imagePath)
        writer.verified = self.verified

        for shape in shapes:
            points = shape['points']
            label = shape['label']
            # Add Chris
            difficult = int(shape['difficult'])
            bndbox = LabelFile.convertPoints2BndBox(points)
            writer.addBndBox(bndbox[0], bndbox[1], bndbox[2], bndbox[3], label, difficult)

        writer.save(targetFile=filename, classList=classList)
        return

    def toggleVerify(self):
        self.verified = not self.verified

